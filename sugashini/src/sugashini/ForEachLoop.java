package sugashini;

public class ForEachLoop {

	public static void main(String[] args) {
		int a[]= {1,2,3,4,5,6};
		for(int p : a) {
			System.out.println(p);
			
		}
		char b[]= {'s','u','g','a','s','h','i','n','i'};
		for(char r:b) {
			System.out.println(r);
		
		}
		String s1[]= {"peri","college"};
		for(String v:s1) {
			System.out.println(v);
		}
		
	}

}
